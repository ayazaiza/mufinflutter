import 'package:cloud_firestore/cloud_firestore.dart';

import '../../../domain/entities/user/mufin_user.dart';

class MufinUserModel extends MufinUser {
  MufinUserModel(
      {required super.name,
      required super.userId,
      required super.firstname,
      required super.lastname,
      required super.description,
      required super.documentId,
      required super.phone,
      required super.email,
      required super.alternateEmail,
      required super.studentRelationShip,
      required super.emailType,
      required super.phoneType,
      required super.address,
      required super.country,
      required super.city,
      required super.state,
      required super.extra,
      required super.howDidYouFindUs,
      required super.timestamp,
      required super.lastUpdated,
      required super.preferredDays,
      required super.preferredTimes,
      required super.receiveEmails,
      required super.receiveSMS,
      required super.admin,
      required super.inspector});

  MufinUserModel copy(
          {String? name,
          String? userId,
          String? firstname,
          String? lastname,
          String? description,
          String? documentId,
          String? phone,
          String? email,
          String? alternateEmail,
          String? studentRelationShip,
          String? emailType,
          String? phoneType,
          String? address,
          String? country,
          String? city,
          String? state,
          String? extra,
          String? howDidYouFindUs,
          Timestamp? timestamp,
          Timestamp? lastUpdated,
          List<String>? preferredDays,
          List<String>? preferredTimes,
          bool? receiveEmails,
          bool? receiveSMS,
          bool? admin,
          bool? inspector}) =>
      MufinUserModel(
          name: name ?? this.name,
          userId: userId ?? this.userId,
          firstname: firstname ?? this.firstname,
          lastname: lastname ?? this.lastname,
          description: description ?? this.description,
          documentId: documentId ?? this.documentId,
          phone: phone ?? this.phone,
          email: email ?? this.email,
          alternateEmail: alternateEmail ?? this.alternateEmail,
          studentRelationShip: studentRelationShip ?? this.studentRelationShip,
          emailType: emailType ?? this.emailType,
          phoneType: phoneType ?? this.phoneType,
          address: address ?? this.address,
          country: country ?? this.country,
          city: city ?? this.city,
          state: state ?? this.state,
          extra: extra ?? this.extra,
          howDidYouFindUs: howDidYouFindUs ?? this.howDidYouFindUs,
          timestamp: timestamp ?? this.timestamp,
          lastUpdated: lastUpdated ?? this.lastUpdated,
          preferredDays: preferredDays ?? this.preferredDays,
          preferredTimes: preferredTimes ?? this.preferredTimes,
          receiveEmails: receiveEmails ?? this.receiveEmails,
          receiveSMS: receiveSMS ?? this.receiveSMS,
          admin: admin ?? this.admin,
          inspector: inspector ?? this.inspector);

  Map<String, dynamic> toJson() {
    return {
      "name": name,
      "userId": userId,
      "firstname": firstname,
      "lastname": lastname,
      "description": description,
      "documentId": documentId,
      "phone": phone,
      "email": email,
      "alternateEmail": alternateEmail,
      "studentRelationShip": studentRelationShip,
      "emailType": emailType,
      "phoneType": phoneType,
      "address": address,
      "country": country,
      "city": city,
      "state": state,
      "extra": extra,
      "howDidYouFindUs": howDidYouFindUs,
      "timestamp": timestamp,
      "lastUpdated": lastUpdated,
      "preferredDays": preferredDays,
      "preferredTimes": preferredTimes,
      "receiveEmails": receiveEmails,
      "receiveSMS": receiveSMS,
      "admin": admin,
      "inspector": inspector
    };
  }

  factory MufinUserModel.toHashMap(Map<String, dynamic> map) => MufinUserModel(
      name: map['name'] ?? '',
      userId: map['userId'] ?? '',
      firstname: map['firstname'] ?? '',
      lastname: map['lastname'] ?? '',
      description: map['description'] ?? '',
      documentId: map['documentId'] ?? '',
      phone: map['phone'] ?? '',
      email: map['email'] ?? '',
      alternateEmail: map['alternateEmail'] ?? '',
      studentRelationShip: map['studentRelationShip'] ?? '',
      emailType: map['emailType'] ?? '',
      phoneType: map['phoneType'] ?? '',
      address: map['address'] ?? '',
      country: map['country'] ?? '',
      city: map['city'] ?? '',
      state: map['state'] ?? '',
      extra: map['extra'] ?? '',
      howDidYouFindUs: map['howDidYouFindUs'],
      timestamp: map['timestamp'] ?? Timestamp.now(),
      lastUpdated: map['lastUpdated'] ?? Timestamp.now(),
      preferredDays: (map['preferredDays'] as List<dynamic>)
          .map((e) => e.toString())
          .toList(),
      preferredTimes: (map['preferredTimes'] as List<dynamic>)
          .map((e) => e.toString())
          .toList(),
      receiveEmails: map['receiveEmails'] ?? true,
      receiveSMS: map['receiveSMS'] ?? true,
      admin: map['admin'] ?? false,
      inspector: map['inspector'] ?? false);
}
